public class Calculator {
    public static int getSum(int firstTerm, int secondTerm) {
        return firstTerm + secondTerm;
    }

    public static int getDifference(int firstTerm, int secondTerm) {
        return firstTerm - secondTerm;
    }

    public static int getMultiplication(int multiplier, int multiplicand) {
        return multiplier * multiplicand;
    }

    public static int getDivision(int dividend, int divisor) {
        return dividend / divisor;
    }
}
